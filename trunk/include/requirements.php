defined("ICMS_ROOT_PATH") or die("ICMS root path not defined");

$failed_requirements = array();

if (ICMS_VERSION_BUILD < 40) {
	$failed_requirements[] = _AM_IMPOLLING_REQUIREMENTS_ICMS_BUILD;
}

if (count($failed_requirements) > 0) {
	icms_cp_header();
	$icmsAdminTpl->assign("failed_requirements", $failed_requirements);
	$icmsAdminTpl->display(IMPOLLING_ROOT_PATH . "templates/impolling_requirements.html");
	icms_cp_footer();
	exit;
}